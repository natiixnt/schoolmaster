                    <span>{new Date(selectedLesson.scheduledAt).toLocaleDateString("pl-PL")} o {new Date(selectedLesson.scheduledAt).toLocaleTimeString("pl-PL", { hour: '2-digit', minute: '2-digit' })}</span>
